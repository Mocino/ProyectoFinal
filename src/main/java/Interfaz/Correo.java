    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        txtCorreo.setText(restaurante.ema);
        String remitente = "restaurante.hyrule@gmail.com";
        String clave = "hyrulerestaurante2020";
        String destino = txtCorreo.getText();
        
        Properties props = new Properties();
        props.put("mail.smtp.host","smtp.gmail.com");
        props.put("mail.smtp.port","587");
        props.put("mail.smtp.auth","true");
        props.put("mail.smtp.starttls.enable","true");
        props.put("mail.smtp.user", remitente); 
        props.put("mail.smtp.clave", clave);
        props.put("mail.smtp.ssl.trust", "smtp.gmail.com");
        
        Session session = Session.getDefaultInstance(props);
        MimeMessage mensaje = new MimeMessage(session);
        
        try{
            mensaje.addRecipient(Message.RecipientType.TO, new InternetAddress(destino));
            mensaje.setSubject("Factura Restaurante Hyrule");
            
            BodyPart parteTexto = new MimeBodyPart();
            parteTexto.setContent("<b>Muchas Gracias por su compra</b>","text/html");
            
            BodyPart parteArchivo = new  MimeBodyPart();
            parteArchivo.setDataHandler(new DataHandler(new FileDataSource(txtRuta.getText())));
            parteArchivo.setFileName("Factura Hyrule.pdf");
            
            MimeMultipart todo = new MimeMultipart();
            todo.addBodyPart(parteTexto);
            todo.addBodyPart(parteArchivo);
            mensaje.setContent(todo);
            Transport transport = session.getTransport("smtp");
            transport.connect("smtp.gmail.com",remitente,clave);
            transport.sendMessage(mensaje, mensaje.getAllRecipients());
            transport.close();
            JOptionPane.showMessageDialog(null, "Factura Enviada");
            JOptionPane.showMessageDialog(null, "Venta HechaÂ¡");
            this.setVisible(false);
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnEnviarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        JFileChooser fc = new JFileChooser();
        int seleccion = fc.showOpenDialog(this);
        
        if (seleccion == JFileChooser.APPROVE_OPTION){
            File fichero = fc.getSelectedFile();
            this.txtRuta.setText(fichero.getAbsolutePath());      
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

